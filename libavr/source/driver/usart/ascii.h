#ifndef ASCII_H
#define ASCII_H

#define CHAR_NUL    0       // Null
#define CHAR_SOH    1       // Start of Heading
#define CHAR_STX    2       // Start of Text
#define CHAR_ETX    3       // End of Text
#define CHAR_EOT    4       // End of Transmission
#define CHAR_ENQ	5		// Enquiry
#define CHAR_ACK	6		// Acknowledge
#define CHAR_BEL    7       // Bell
#define CHAR_BS     8       // Backspace
#define CHAR_TAB	9		// Horizontal Tab
#define CHAR_HT     9       // Horizontal Tab
#define CHAR_LF		10		// Line Feed
#define CHAR_VT     11      // Vertical Tab
#define CHAR_FF     12      // Form Feed
#define CHAR_CR		13		// Carriage Return
#define CHAR_SO		14		// Shift Out
#define CHAR_SI     15      // Shift In
#define CHAR_DLE    16      // Data Link Escape
#define CHAR_DC1    17      // Device Control 1
#define CHAR_XON	17		// Transmit On
#define CHAR_DC2    18      // Device Control 2
#define CHAR_DC3    19      // Device Control 3
#define CHAR_XOFF	19		// Transmit Off
#define CHAR_DC4    20      // Device Control 4
#define CHAR_NAK	21		// Negative Acknowledge
#define CHAR_SYN	22		// Synchronous Idle
#define CHAR_ETB    23      // End of Transmission Block
#define CHAR_CAN    24      // Cancel
#define CHAR_EM     25      // End of Medium
#define CHAR_SUB    26      // Substitute
#define CHAR_ESC    27      // Escape
#define CHAR_FS     28      // File Seperator
#define CHAR_GS     29      // Group Seperator
#define CHAR_RS     30      // Record Seperator
#define CHAR_US     31      // Unit Seperator

#endif


